// دالة للحصول على معلومات السياسة حسب النوع
export function getPolicyInfo(type: string) {
  switch (type) {
    case 'delivery':
      return appPolicies.delivery;
    case 'return':
      return appPolicies.returnPolicy;
    case 'terms':
      return appPolicies.termsAndConditions;
    case 'app':
      return appPolicies.appInfo;
    case 'faq':
      return appPolicies.faq;
    default:
      return appPolicies;
  }
}